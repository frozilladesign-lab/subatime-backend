type TimeBlock = { start: string; end: string; label: string };
export type ContextKey = 'overall' | 'career' | 'love' | 'health';

/**
 * Marks predictions generated with the corrected `buildChartInputFromBirthProfile()` (local
 * birth time, not a UTC slice). Bump this string whenever chart-input derivation changes again
 * so old rows are recognized as stale and regenerated on next read.
 *
 * Migration/backfill note: predictions generated before `chartInputVersion: "local-birth-time-v2"`
 * may contain incorrect lagna/nakṣatra text for non-UTC profiles. They are NOT deleted or
 * rewritten automatically — they should be regenerated by natural cache invalidation (handled in
 * `getTodayForUser`/`generateForUser` below) or, if immediate correction across all users is
 * required, by a separately reviewed one-time backfill job. No automatic production backfill is
 * run by this code.
 */
export const CURRENT_CHART_INPUT_VERSION = 'local-birth-time-v2';

/** True when a stored prediction's chart-input version is missing or not the current one. */
export function isChartInputVersionStale(chartInputVersion: string | null | undefined): boolean {
  return chartInputVersion !== CURRENT_CHART_INPUT_VERSION;
}

/**
 * Pure mapping from a stored birth profile to the chart engine's input shape.
 *
 * `dateOfBirth`/`timeOfBirth` are stored as UTC instants; `birthLocalDate`/`birthLocalTime`
 * preserve the original local wall-clock values as entered by the user. The chart engine
 * interprets `birthTime` as local time *within* `timezone` (see `JyotishaChartEngine`'s
 * `toUtcBirthMoment`), so feeding it a UTC-sliced `HH:mm` here would double-convert the
 * timezone offset and silently compute the wrong ascendant/nakṣatra — this was a real, shipped
 * bug (see `daily-prediction.service.spec.ts`'s `buildChartInputFromBirthProfile` regression
 * test, which reproduces it with a real birth profile from a year `Asia/Colombo` was UTC+06:00,
 * not the now-current UTC+05:30, to guarantee a naive UTC slice would visibly diverge).
 *
 * Always prefer `birthLocalDate`/`birthLocalTime`; only fall back to the UTC instant for
 * legacy rows that predate those columns.
 *
 * Cache safety: predictions generated before this fix may have incorrect lagna/nakṣatra for
 * non-UTC profiles (the persisted `summary` text could disagree with `meta.lagna`/
 * `meta.nakshatra`, which are recomputed correctly on read) and should be regenerated.
 */
export function buildChartInputFromBirthProfile(
  fullName: string,
  profile: {
    dateOfBirth: Date;
    timeOfBirth: Date;
    birthLocalDate?: string | null;
    birthLocalTime?: string | null;
    placeOfBirth: string;
    latitude: number;
    longitude: number;
    timezone: string | null;
    userKnownLagna?: string | null;
  },
): GenerateChartDto {
  return {
    fullName,
    birthDate: profile.birthLocalDate?.trim() || profile.dateOfBirth.toISOString().slice(0, 10),
    birthTime: profile.birthLocalTime?.trim() || profile.timeOfBirth.toISOString().slice(11, 16),
    birthPlace: profile.placeOfBirth,
    latitude: profile.latitude,
    longitude: profile.longitude,
    timezone: profile.timezone ?? undefined,
    lagnaUserOverride: profile.userKnownLagna?.trim() || undefined,
  };
}

export interface PredictionPersonalization {
  mostRelevantContext: ContextKey;
  lowerSignalContexts: ContextKey[];
  contextWeights: Record<string, number>;
  /**
   * Accent on block scores (~0.90–1.10): maps dominant `contextWeights[mostRelevantContext]`
   * so feedback history nudges timing without crushing raw transit scores.
   */
  primaryContextScoreMultiplier: number;
}

export interface DailyPredictionOutput {
  /** Row id in `daily_predictions` (POST `/predictions/:id/feedback`). */
  predictionId: string;
  userId: string;
  date: string;
  summary: string;
  goodTimes: TimeBlock[];
  badTimes: TimeBlock[];
  transits: DayTransitDto[];
  confidenceScore: number;
  personalization: PredictionPersonalization;
  /**
   * Single source of notification wording for this day (engine output). Delivery layers —
   * backend FCM schedulers and the Flutter local scheduler — select from this and never
   * compose astrology copy themselves.
   */
  notificationCandidates?: NotificationCandidates;
  meta: {
    lagna: string;
    nakshatra: string;
    scoreSpread: number;
    method:
      | 'weighted-score-v2'
      | 'weighted-score-v2+gemini'
      | 'weighted-score-v3'
      | 'weighted-score-v3-local'
      | 'weighted-score-v3+gemini';
    /** Sidereal Moon sign (Janma Rāśi) from birth chart. */
    janmaRasi?: string;
    /** Tara Bāla at local civil noon in birth-profile timezone (approximate daily snapshot). */
    taraAtBirthTimezoneNoon?: { index1To9: number; name: string };
    /** Saturn's current transit house counted from Janma Rāśi (1–12). Sade Sati = houses 12, 1, 2. */
    saturnTransitHouseFromMoon?: number;
    /** True when Saturn is in Sade Sati position (houses 12, 1, or 2 from Janma Rāśi). */
    sadeSati?: boolean;
    /** Jupiter's current transit house counted from Janma Rāśi (1–12). House 11 is most auspicious. */
    jupiterTransitHouseFromMoon?: number;
    /** Current Antara (sub-period) lord within the Mahādaśā. */
    antaraLord?: string;
    /**
     * True when the underlying chart was produced via the legacy-ephemeris fallback
     * (Swiss Ephemeris threw). Legacy ascendant and Mars/Mercury/Venus longitudes are
     * known-inaccurate, so lagna, dasha lord, Manglik dosha, and house placements may be
     * wrong — surfaced so the UI can show a "chart needs verification" indicator.
     */
    chartNeedsVerification?: boolean;
    /**
     * Human-readable warning surfaced whenever `chartNeedsVerification` is true — UI-safe text
     * explaining that the chart used an approximate calculation path, so this prediction must
     * not be presented as high-confidence.
     */
    accuracyWarning?: string;
  };
}

